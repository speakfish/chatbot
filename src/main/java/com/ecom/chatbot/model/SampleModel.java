package com.ecom.chatbot.model;

public class SampleModel {
    private Long   id;
    private String name;
    private String selectDateView;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSelectDateView() {
        return selectDateView;
    }

    public void setSelectDateView(String selectDateView) {
        this.selectDateView = selectDateView;
    }
}
